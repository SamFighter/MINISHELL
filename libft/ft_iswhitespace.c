/**
 * Return 1 if the input character is a white space,
 * else return 0
 */
int	ft_iswhitespace(int c)
{
	if ((c == ' ' || c == '\t' || c == '\r')
		|| c == '\n' || c == '\v' || c == '\f')
		return (1);
	return (0);
}
